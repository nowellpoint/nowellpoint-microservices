package com.nowellpoint.identity.entity;

import java.io.Serializable;
import java.util.Date;

public class Address implements Serializable {

	private static final long serialVersionUID = 8625374697414134673L;
	
	private String id;
	
	private String street;

	private String city;
	
	private String state;
	
	private String stateCode;

	private String postalCode;

	private String countryCode;
	
	private String latitude;

	private String longitude;
	
	private Date addedOn;
	
	private Date updatedOn;

	public Address() {
		
	}
	
	private Address(String countryCode) {
		this.countryCode = countryCode;
	}
	
	public static Address of(String countryCode) {
		return new Address(countryCode);
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getStateCode() {
		return stateCode;
	}

	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}

	public String getPostalCode() {
		return postalCode;
	}

	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}

	public String getCountryCode() {
		return countryCode;
	}

	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public Date getAddedOn() {
		return addedOn;
	}

	public void setAddedOn(Date addedOn) {
		this.addedOn = addedOn;
	}

	public Date getUpdatedOn() {
		return updatedOn;
	}

	public void setUpdatedOn(Date updatedOn) {
		this.updatedOn = updatedOn;
	}
}